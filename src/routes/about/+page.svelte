<h2>About</h2>

<p>
    This site is a quick way for me to get current wind speed info to know if I can fly my small quadcopters outside.
</p>

<p>
    It also shows any days in the next 7 that have near perfect conditions,
    though these rarely show up as the forecast often predicts higher wind speeds than might actually materialize.
</p>

<hr/>

<h2>FAQ</h2>

<h3>Why not just use a weather app?</h3>
<p>
    Weather apps I've tried all have wind speed and gust info tucked behind 2 or 3 clicks
    (usually 5+ for forecasts beyond today).
    With a free weather api and some data parsing I can surface this info on page 1 to get all relevant info at a glance.
</p>

